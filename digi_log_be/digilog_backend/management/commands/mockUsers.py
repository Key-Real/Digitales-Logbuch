class Command(BaseCommand):
    help = 'Import Course Data'

    def add_arguments(self, parser):
        super(Command, self).add_arguments(parser)
        parser.add_argument(
            '--file',
            dest='file',
            default=None,
            help='json file to import from',
        )

    def handle(self, *args, **options):
        if len(User.objects.all())>1:
            print(f"{len(User.objects.all())} Users already exist")
            return
        courses = Course.objects.all()
        for i in range(1000):
            try:
                vorname = random.choice(vornamen)
                nachname = random.choice(nachnamen)
                uname = vorname[0]+nachname
            except Exception as e:
                print(f"{vorname}, {nachname}")
                print(vornamen.index(vorname))
                print(e)
                return
            user, created = User.objects.get_or_create(username=uname, defaults={"password": uname, "email":uname+"@example.com", "first_name":vorname, "last_name":nachname,})

            if len(courses) > 0 and created:
                for j in range(random.randint(1, 10)):
                    course = random.choice(courses)
                    try:
                        Attendee.objects.create(
                            attends=random.choice([True, False]),
                            attendee=user,
                            course=course
                        ).save()
                    except Exception as e:
                        print(e)
                
                user.save()